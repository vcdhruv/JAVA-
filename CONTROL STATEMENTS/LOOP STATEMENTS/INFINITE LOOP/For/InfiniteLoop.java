//If you use two semicolons ;; in the for loop, it will be infinitive for loop.
/*Syntax :
for(;;){  
//code to be executed  
} 
*/
public class InfiniteFor{  
public static void main(String[] args) {  
    //Using no condition in for loop  
    for(;;){  
        System.out.println("infinitive loop");  
    }  
}  
}  
