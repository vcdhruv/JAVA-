public class Overflow{  
public static void main(String[] args){  
                                        //Overflow  
                                        int a=130;  
                                        byte b=(byte)a;  
                                        System.out.println(a);  
                                        System.out.println(b);  
                                       }
}  
//A java variable overflow is when the data stored as a variable exceeds the maximum amount of data it can store.